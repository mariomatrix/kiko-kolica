def prune_kom_previews():
    now = datetime.now()
    expired = [
        token for token, payload in kom_import_previews.items()
        if payload["expires_at"] <= now
    ]
    for token in expired:
        kom_import_previews.pop(token, None)


def normalize_excel_barcode(value):
    return str(value or "").strip().upper()


def normalize_excel_header(value):
    text = str(value or "")
    text = text.replace("\ufeff", " ").replace("\u00a0", " ")
    text = text.replace("_", " ")
    text = re.sub(r"\s+", " ", text).strip().lower()
    return text


def parse_kom_value(value):
    try:
        number = int(float(str(value).strip()))
    except (TypeError, ValueError):
        return None
    return number if number >= 1 else None


def build_kom_preview_for_tura(tura: str, filename: str, excel_bytes: bytes):
    try:
        raw_df = pd.read_excel(BytesIO(excel_bytes), sheet_name="Instances", header=None, dtype=object)
    except ValueError as exc:
        raise HTTPException(status_code=400, detail="Excel nema sheet 'Instances'.") from exc
    except Exception as exc:
        raise HTTPException(status_code=400, detail=f"Excel nije moguće učitati: {exc}") from exc

    header_row_index = None
    required_headers = {
        normalize_excel_header("opc_barkod_potrebe"),
        normalize_excel_header("BROJ MAX"),
    }
    scan_limit = min(len(raw_df), 50)
    for idx in range(scan_limit):
        row_values = {
            normalize_excel_header(value)
            for value in raw_df.iloc[idx].tolist()
            if normalize_excel_header(value)
        }
        if required_headers.issubset(row_values):
            header_row_index = idx
            break

    if header_row_index is None:
        sample_rows = []
        for idx in range(min(len(raw_df), 5)):
            values = [
                str(value).strip()
                for value in raw_df.iloc[idx].tolist()
                if str(value).strip() and str(value).strip().lower() != "nan"
            ]
            if values:
                sample_rows.append(" | ".join(values[:8]))
        preview = " || ".join(sample_rows) if sample_rows else "nema čitljivih redova"
        raise HTTPException(
            status_code=400,
            detail=(
                "Excel nema obavezne kolone: opc_barkod_potrebe, BROJ MAX. "
                f"Sheet 'Instances' je učitan, ali header red nije pronađen. "
                f"Prvi redovi izgledaju ovako: {preview}"
            ),
        )

    df = raw_df.iloc[header_row_index + 1 :].copy()
    df.columns = [str(col).strip() for col in raw_df.iloc[header_row_index].tolist()]
    df.columns = [str(col).strip() for col in df.columns]
    normalized_columns = {normalize_excel_header(col): col for col in df.columns}

    def resolve_excel_column(*aliases: str):
        for alias in aliases:
            resolved = normalized_columns.get(normalize_excel_header(alias))
            if resolved:
                return resolved
        return None

    barcode_column = resolve_excel_column("opc_barkod_potrebe", "opc barkod potrebe")
    kom_column = resolve_excel_column("BROJ MAX", "BROJ_MAX", "broj max", "broj_max")

    missing = []
    if not barcode_column:
        missing.append("opc_barkod_potrebe")
    if not kom_column:
        missing.append("BROJ MAX")
    if missing:
        available_columns = ", ".join(df.columns[:12])
        if len(df.columns) > 12:
            available_columns += ", ..."
        raise HTTPException(
            status_code=400,
            detail=(
                f"Excel nema obavezne kolone: {', '.join(missing)}. "
                f"Nađeni stupci: {available_columns}"
            )
        )

    with get_db() as conn:
        rows = conn.execute(
            "SELECT * FROM dijelovi WHERE tura = %s ORDER BY id",
            (tura,)
        ).fetchall()

    if not rows:
        raise HTTPException(status_code=404, detail=f"Tura '{tura}' nije pronađena.")

    row_dicts = [dict(row) for row in rows]
    rows_by_barcode: dict[str, list[dict]] = {}
    for row in row_dicts:
        barcode = normalize_excel_barcode(row["barcode"])
        rows_by_barcode.setdefault(barcode, []).append(row)

    invalid_rows = []
    grouped: dict[str, dict] = {}
    total_rows = len(df)

    for idx, raw in df.iterrows():
        barcode = normalize_excel_barcode(raw.get(barcode_column))
        if not barcode:
            invalid_rows.append({
                "row_number": int(idx) + 2,
                "barcode": "",
                "reason": f"Prazan {barcode_column}",
            })
            continue
        kom_value = parse_kom_value(raw.get(kom_column))
        if kom_value is None:
            invalid_rows.append({
                "row_number": int(idx) + 2,
                "barcode": barcode,
                "reason": f"Neispravan {kom_column}: {raw.get(kom_column)}",
            })
            continue
        bucket = grouped.setdefault(
            barcode,
            {"kom_values": set(), "row_numbers": [], "raw_count": 0}
        )
        bucket["kom_values"].add(kom_value)
        bucket["row_numbers"].append(int(idx) + 2)
        bucket["raw_count"] += 1

    conflicts = []
    missing_in_tura = []
    clean_matches = []
    unchanged_matches = 0
    changed_row_count = 0
    updates = []

    for barcode, payload in grouped.items():
        kom_values = sorted(payload["kom_values"])
        if len(kom_values) > 1:
            conflicts.append({
                "barcode": barcode,
                "kom_values": kom_values,
                "row_numbers": payload["row_numbers"],
            })
            continue

        target_rows = rows_by_barcode.get(barcode, [])
        if not target_rows:
            missing_in_tura.append({
                "barcode": barcode,
                "kom": kom_values[0],
            })
            continue

        new_kom = kom_values[0]
        changed_ids = []
        sample_old_kom = None
        for row in target_rows:
            old_kom = max(int(row["kom"] or 1), 1)
            if sample_old_kom is None:
                sample_old_kom = old_kom
            if old_kom != new_kom:
                changed_ids.append(int(row["id"]))
        if changed_ids:
            changed_row_count += len(changed_ids)
        else:
            unchanged_matches += 1

        updates.append({
            "barcode": barcode,
            "kom": new_kom,
            "row_ids": [int(row["id"]) for row in target_rows],
        })
        clean_matches.append({
            "barcode": barcode,
            "kom": new_kom,
            "match_count": len(target_rows),
            "old_kom": sample_old_kom,
            "changed": bool(changed_ids),
        })

    token = uuid4().hex
    now = datetime.now()
    kom_import_previews[token] = {
        "token": token,
        "tura": tura,
        "filename": filename,
        "generated_at": now,
        "expires_at": now + timedelta(minutes=KOM_PREVIEW_TTL_MINUTES),
        "updates": updates,
    }

    return {
        "token": token,
        "tura": tura,
        "filename": filename,
        "sheet": "Instances",
        "stats": {
            "excel_rows_total": total_rows,
            "unique_barcodes": len(grouped),
            "invalid_rows": len(invalid_rows),
            "conflict_barcodes": len(conflicts),
            "missing_in_tura": len(missing_in_tura),
            "clean_matches": len(clean_matches),
            "changed_rows": changed_row_count,
            "unchanged_matches": unchanged_matches,
        },
        "conflicts": conflicts[:50],
        "missing_in_tura_details": missing_in_tura[:50],
        "invalid_rows_details": invalid_rows[:50],
        "sample_matches": clean_matches[:50],
    }


