@app.post("/kom-import/apply")
def apply_kom_import(body: KomImportApply, request: Request):
    require_admin(request)
    prune_kom_previews()
    preview = kom_import_previews.get(body.token)
    if not preview:
        raise HTTPException(status_code=404, detail="Preview token nije pronađen ili je istekao.")

    with get_db() as conn:
        updated_rows = 0
        updated_barcodes = 0
        for item in preview["updates"]:
            new_kom = int(item["kom"])
            for row_id in item["row_ids"]:
                row = conn.execute("SELECT * FROM dijelovi WHERE id = %s", (row_id,)).fetchone()
                if not row:
                    continue
                scan_count = min(max(int(row["scan_count"] or 0), 0), new_kom)
                status = "SAVIJENO" if scan_count > 0 else "IZREZANO"
                vrijeme = row["vrijeme"]
                if scan_count == 0:
                    vrijeme = None
                conn.execute(
                    "UPDATE dijelovi SET kom = %s, scan_count = %s, status = %s, vrijeme = %s WHERE id = %s",
                    (new_kom, scan_count, status, vrijeme, row_id)
                )
                updated_rows += 1
            updated_barcodes += 1

        conn.commit()
    
    kom_import_previews.pop(body.token, None)

    return {
        "ok": True,
        "tura": preview["tura"],
        "updated_barcodes": updated_barcodes,
        "updated_rows": updated_rows,
        "filename": preview["filename"],
    }


