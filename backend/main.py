@app.get("/scan/{barcode}")
def scan_barcode(barcode: str, request: Request):
    """
    Pretraži sve ture za barcode.
    Vrati prvi IZREZANO redak — ili info da nema IZREZANO.
    """
    user = require_auth(request)
    with get_db() as conn:
        # Sve koji odgovaraju barcodeu
        all_rows = conn.execute(
            "SELECT * FROM dijelovi WHERE LOWER(barcode) = LOWER(%s)",
            (barcode,)
        ).fetchall()

        if not all_rows:
            raise HTTPException(status_code=404, detail=f"Barcode '{barcode}' nije pronađen ni u jednoj turi.")

        # Traži i zaključaj prvi IZREZANO s slobodnim skeniranjima
        target = conn.execute(
            """
            SELECT * FROM dijelovi 
            WHERE LOWER(barcode) = LOWER(%s)
              AND status = 'IZREZANO'
              AND scan_count < kom
            ORDER BY id
            FOR UPDATE SKIP LOCKED
            LIMIT 1
            """,
            (barcode,)
        ).fetchone()

        if not target:
            statusi = list({r["status"] for r in all_rows})
            sample_row = format_row_vrijeme(dict(all_rows[0]))
            kom = max(int(sample_row["kom"] or 1), 1)
            scan_count = min(max(int(sample_row["scan_count"] or 0), 0), kom)
            return {
                "found": True,
                "has_izrezano": False,
                "total": len(all_rows),
                "statusi": statusi,
                "row": sample_row,
                "completed_progress": f"{scan_count} od {kom}",
            }

        kom = max(int(target["kom"] or 1), 1)
        current_scan_count = min(max(int(target["scan_count"] or 0), 0), kom)
        new_scan_count = min(current_scan_count + 1, kom)
        scan_time = datetime.now(timezone.utc)
        
        conn.execute(
            """
            UPDATE dijelovi 
            SET status = 'SAVIJENO', scan_count = %s, vrijeme = %s, operater = %s 
            WHERE id = %s
            """,
            (new_scan_count, scan_time, user["display_name"], target["id"])
        )
        conn.commit()

        updated = conn.execute(
            "SELECT * FROM dijelovi WHERE id = %s", (target["id"],)
        ).fetchone()

    return {
        "found": True,
        "has_izrezano": True,
        "remaining_scans": max(kom - new_scan_count, 0),
        "row": format_row_vrijeme(dict(updated))
    }


@app.patch("/dijelovi/{id}/status")
def update_status(id: int, body: StatusUpdate, request: Request):
    """Ručna promjena statusa. Ako status nije SAVIJENO, briše kolica."""
    require_admin(request)
    with get_db() as conn:
        row = conn.execute("SELECT * FROM dijelovi WHERE id = %s", (id,)).fetchone()
        if not row:
            raise HTTPException(status_code=404, detail="Redak nije pronađen.")

        # Ako se mijenja s SAVIJENO na nešto drugo — briši kolica
        kom = max(int(row["kom"] or 1), 1)
        scan_count = min(max(int(row["scan_count"] or 0), 0), kom)
        kolica = row["kolica"]
        if row["status"] == "SAVIJENO" and body.status != "SAVIJENO":
            kolica = None

        vrijeme = row["vrijeme"]
        if body.status == "IZREZANO":
            scan_count = 0
            vrijeme = None
        elif body.status == "SAVIJENO":
            scan_count = kom
            if not vrijeme:
                vrijeme = datetime.now(timezone.utc)

        conn.execute(
            "UPDATE dijelovi SET status = %s, kolica = %s, scan_count = %s, vrijeme = %s WHERE id = %s",
            (body.status, kolica, scan_count, vrijeme, id)
        )
        conn.commit()
        updated = conn.execute("SELECT * FROM dijelovi WHERE id = %s", (id,)).fetchone()
        
    return format_row_vrijeme(dict(updated))


