def get_current_user_or_none(request: Request):
    auth = request.session.get("auth")
    if not auth:
        return None

    try:
        with get_db() as conn:
            row = conn.execute(
                """
                SELECT username, role, display_name, active
                FROM korisnici
                WHERE username = %s
                """,
                (auth.get("username"),),
            ).fetchone()
    except Exception:
        return None

    if not row or not row["active"]:
        request.session.pop("auth", None)
        return None

    return {
        "username": row["username"],
        "role": row["role"],
        "display_name": row["display_name"] or row["username"],
    }


